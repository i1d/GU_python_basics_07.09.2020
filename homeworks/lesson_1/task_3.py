"""
3. Узнайте у пользователя число n. Найдите сумму чисел n + nn + nnn.
Например, пользователь ввёл число 3. Считаем 3 + 33 + 333 = 369.
"""
n = str(input("Please enter integer N: "))
_n_1 = int(n)
_n_2 = int(n * 2)
_n_3 = int(n * 3)
print(f'Result of "N + NN + NNN" ({_n_1} + {_n_2} + {_n_3}) = {_n_1 + _n_2 + _n_3}')